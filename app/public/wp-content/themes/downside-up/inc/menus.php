/**
 * Fallback navigation used when no menu has been assigned to the
 * "Primary Menu" location in Appearance > Menus.
 */
function downside_up_default_menu()
{
    $links = [
        __('Services', 'downside-up') => home_url('/services'),
        __('About', 'downside-up')    => home_url('/about'),
        __('Blog', 'downside-up')      => home_url('/blog'),
    ];

    echo '<ul class="primary-nav__list">';
    foreach ($links as $label => $url) {
        printf('<li><a href="%s">%s</a></li>', esc_url($url), esc_html($label));
    }
    echo '</ul>';
}

